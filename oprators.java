class oprators {
    public static void main(String[]args){
        int number1 = 12;
        int number2 = 6;

        // operating two intergers will print out interger value
        System.out.println(number1 + number2);
        System.out.println(number1 - number2);
        System.out.println(number1 / number2);
        System.out.println(number1 * number2);
        System.out.println(number1 % number2);
    }
}

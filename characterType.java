class characterType {
    public static void main(String[]args){

        char percent = '&';
        // character data type
        System.out.println(percent);
    }
    
}

class string {
    public static void main(String[]args){

        String name = "Happy Family";
        // Sring data type
        System.out.println(name);
    }
    
}
class newString {
    public static void main(String[]args){
        // Other ways to write string using new string value.
        String name = new String ("Happy Family");

        System.out.println(name);
    }
    
}

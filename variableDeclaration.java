public class variableDeclaration {
    public static void main(String args[]){
        //Declaring a variable named num
        int num = 10;
        int _num = 20;
        int $num = 30;
        int num123 = 40;
        int NUM = 50;
        int myNum = 100;
        int num_last = 1000;

        //printing the value of the variables num

        System.out.println("value if the variable num: " +num);
        System.out.println("value if the variable num: "+_num);
        System.out.println("value if the variable num: " +$num);
        System.out.println("value if the variable num: " +num123);
        System.out.println("value if the variable num: " +NUM);
        System.out.println("value if the variable num: " +myNum);
        System.out.println("value if the variable num: " +num_last);
    }
}

class Comparator {
    public static void main(String[]args){
        int number1 = 18;
        int number2 = 20;

        // not equal to
        System.out.println(number1 != number2);
        
        // equal to
        System.out.println(number1 == number2);

        // greater than
        System.out.println(number1 > number2);

        // less than
        System.out.println(number1 < number2);

        // greater than or equal to
        System.out.println(number1 >= number2);

        // less than or equal to
        System.out.println(number1 <= number2);
        
    }
    
}
